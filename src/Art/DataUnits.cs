namespace Art;

/// <summary>
/// Specifies the type of units to use when displaying data units.
/// </summary>
public enum DataUnits
{
    /// <summary>
    /// Use binary units (bytes, kilobytes, megabytes, etc.).
    /// </summary>
    Binary,
    /// <summary>
    /// Use decimal units (bytes, kibibytes, mebibytes, etc.).
    /// </summary>
    Decimal,
}
